    UNIQUE KEY `uk_user_period` (`user_id`, `period_type`, `start_date`),
    KEY `idx_user_id` (`user_id`),
    KEY `idx_dept_id` (`dept_id`),
    KEY `idx_period_type` (`period_type`),
    KEY `idx_start_date` (`start_date`),
    KEY `idx_end_date` (`end_date`),
    KEY `idx_year_month` (`year_month`),
    KEY `idx_year` (`year`),
    KEY `idx_is_final` (`is_final`),
    KEY `idx_create_time` (`create_time`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='Attendance statistics table';

