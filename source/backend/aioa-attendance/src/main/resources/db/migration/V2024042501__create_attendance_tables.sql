    `start_date` date NOT NULL COMMENT 'Start date',
    `end_date` date NOT NULL COMMENT 'End date',
    `year_month` date DEFAULT NULL COMMENT 'Year and month (for monthly stats)',
    `quarter` tinyint DEFAULT NULL COMMENT 'Quarter (1-4)',
    `year` int DEFAULT NULL COMMENT 'Year',
    `total_work_days` int DEFAULT '0' COMMENT 'Total work days',
    `actual_work_days` int DEFAULT '0' COMMENT 'Actual work days',
    `normal_days` int DEFAULT '0' COMMENT 'Normal days',
    `late_days` int DEFAULT '0' COMMENT 'Late days',
    `leave_early_days` int DEFAULT '0' COMMENT 'Leave early days',
    `absent_days` int DEFAULT '0' COMMENT 'Absent days',
    `overtime_days` int DEFAULT '0' COMMENT 'Overtime days',
    `leave_days` int DEFAULT '0' COMMENT 'Leave days',
    `business_trip_days` int DEFAULT '0' COMMENT 'Business trip days',
    `total_late_minutes` int DEFAULT '0' COMMENT 'Total late minutes',
    `total_leave_early_minutes` int DEFAULT '0' COMMENT 'Total leave early minutes',
    `total_overtime_minutes` int DEFAULT '0' COMMENT 'Total overtime minutes',
    `total_work_hours` decimal(10,2) DEFAULT '0.00' COMMENT 'Total work hours',
    `should_work_hours` decimal(10,2) DEFAULT '0.00' COMMENT 'Should work hours',
    `normal_rate` decimal(5,2) DEFAULT '0.00' COMMENT 'Normal rate (percentage)',
    `overtime_rate` decimal(5,2) DEFAULT '0.00' COMMENT 'Overtime rate (percentage)',
    `attendance_score` decimal(5,2) DEFAULT '0.00' COMMENT 'Attendance score (0-100)',
